package modelo;

public class Usuario {

	private String user;
	private String password;
	
	
	public Usuario(String user, String password) {
		super();
		this.user = user;
		this.password = password;
	}


	public String getUser() {
		return user;
	}


	public String getPassword() {
		return password;
	}
	
	
	
	
	
	
}
